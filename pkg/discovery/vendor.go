// GetMACVendor gibt den Vendor-Namen für eine gegebene MAC-Adresse zurück OUI
func GetMACVendor(mac string) string {
	if len(mac) < 8 {
		return ""
	}

	oui := strings.ToUpper(mac[:8]) // First 3 octets: "AA:BB:CC"

	if vendor, exists := ouiDatabase[oui]; exists {
		return vendor
	}

	return ""
}

